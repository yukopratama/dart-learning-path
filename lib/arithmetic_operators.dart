void main() {
  var angka1 = 50;
  var angka2 = 2;

  print(
      angka1.toString() + " + " + angka2.toString() + " = ${angka1 + angka2}");
  print(
      angka1.toString() + " - " + angka2.toString() + " = ${angka1 - angka2}");
  print(
      angka1.toString() + " x " + angka2.toString() + " = ${angka1 * angka2}");
  print(
      angka1.toString() + " : " + angka2.toString() + " = ${angka1 / angka2}");
  print(
      angka1.toString() + " % " + angka2.toString() + " = ${angka1 % angka2}");
}
